package org.niord.core.promulgation.vo;

import org.niord.model.IJsonSerializable;
import org.niord.model.message.AreaVo;

import java.util.ArrayList;
import java.util.List;

/**
 * Defines a NAVTEX transmitter station
 */
public class NavtexTransmitterVo implements IJsonSerializable {

    String name;
    boolean active;
    List<AreaVo> areas = new ArrayList<>();

    /*************************/
    /** Getters and Setters **/
    /*************************/

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public boolean isActive() {
        return active;
    }

    public void setActive(boolean active) {
        this.active = active;
    }

    public List<AreaVo> getAreas() {
        return areas;
    }

    public void setAreas(List<AreaVo> areas) {
        this.areas = areas;
    }
}
