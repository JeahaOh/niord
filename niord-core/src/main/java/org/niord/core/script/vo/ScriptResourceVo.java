package org.niord.core.script.vo;

import org.niord.core.script.ScriptResource;
import org.niord.model.IJsonSerializable;

/**
 * Defines the value object for a database-backed Freemarker template and JavaScript resources
 */
@SuppressWarnings("unused")
public class ScriptResourceVo implements IJsonSerializable {

    Integer id;
    ScriptResource.Type type;
    String path;
    String content;

    /*************************/
    /** Getters and Setters **/
    /*************************/

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public ScriptResource.Type getType() {
        return type;
    }

    public void setType(ScriptResource.Type type) {
        this.type = type;
    }

    public String getPath() {
        return path;
    }

    public void setPath(String path) {
        this.path = path;
    }

    public String getContent() {
        return content;
    }

    public void setContent(String content) {
        this.content = content;
    }
}
